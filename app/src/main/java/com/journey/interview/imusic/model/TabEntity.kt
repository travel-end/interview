package com.journey.interview.imusic.model

import com.journey.interview.customizeview.tablayout.CustomTab


class IMusicTab(private var title: String, private var selectedIcon: Int = 0, private var unSelectedIcon: Int = 0) :
    CustomTab {

    override fun getTabTitle(): String {
        return title
    }

    override fun getTabSelectedIcon(): Int {
        return selectedIcon
    }

    override fun getTabUnselectedIcon(): Int {
        return unSelectedIcon
    }

}